// 💡 единая функция обновления состояния
function setZone(key) {
  const item = data[key];
  if (!item) return;

  title.textContent = item.title;
  text.textContent = item.text;
  img.src = item.img;

  zones.forEach(z => z.classList.remove('active'));

  const activeZone = document.querySelector(`[data-zone="${key}"]`);
  if (activeZone) {
    activeZone.classList.add('active');
  }
}

// 🖱️ клики по зонам
zones.forEach(zone => {
  zone.addEventListener('click', () => {
    setZone(zone.dataset.zone);
  });
});

// 🚀 стартовое состояние (Ресепшн = admin)
window.addEventListener('DOMContentLoaded', () => {
  setZone('admin');
});

const mobileButtons = document.querySelectorAll('.mobile-map button');

mobileButtons.forEach(btn => {
  btn.addEventListener('click', () => {
    setZone(btn.dataset.zone);

    mobileButtons.forEach(b => b.classList.remove('active'));
    btn.classList.add('active');
  });
});